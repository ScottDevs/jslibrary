// While Loop

//Make a variable called: roomCapacity
//A room can hold up to 50 people.
var roomCapacity = 0; //Starting point is 0.

while(roomCapacity < 50){
	roomCapacity++;
	console.log(roomCapacity + ":" + " People currently in the room.");
	//This uses a boolean of true or false. 
	//as long as roomCapacity is less than 50, then add++ (1) until it reaches 50.
}